#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*last;

	last = NULL;
	while (*s != '\0')
	{
		if (*s == (const char)c)
		{
			last = s;
		}
		s++;
	}
	if (*s == (const char)c)
	{
		last = s;
	}
	return ((char *)last);
}
